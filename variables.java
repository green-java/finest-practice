 import java.lang.*;
 class variables
 {
    public static void main(String args[])
    {
        String name="hello"; //string datatype using double quote
        int age=25; //int type
        float percent=25.25f; //float type
        char gender='m'; //char type using single quote
        boolean married =false; //boolean type 
        //boolean  default value is false
        System.out.println("Name: "+name);
        System.out.println("Age: "+age);
        System.out.println("Percentage: "+percent);
        System.out.println("Gender type: "+gender);
        System.out.println("Married statues: "+married);
    }
 }