public class assignment {
    public static void main(String args[])
    {
        int a=123;
        System.out.println("Value of a  : "+(a)); 
        a+=10;      //assigning  a=a+10
        System.out.println("add 10 : "+(a)); 
        a-=10;       //assigning  a=a-10 
        System.out.println("sub 10  : "+(a));
        a*=10;      //assigning  a=a*10   
        System.out.println("Mul 10 : "+(a));  
        a/=10;      //assigning  a=a/10 
        System.out.println("Div 10 : "+(a));    
        a%=10;     //assigning  a=a%10
        System.out.println("Mod 10 : "+(a));     
    }
}
