import java.lang.*;
class casting {
    public static void main(String args[])
    {
        int a=10;   // assigning 'a' value as int
        double b=a;      //change int value as double
        double d=25.345;    // assigning 'd' value as double
        int c=(int)d;    //casting from double to int
        System.out.println("Integer : "+a);
        System.out.println("casting int value into Double: "+b);
        System.out.println("Double: "+d);
        System.out.println(" casting Double value int integer: "+c);
    }
}
