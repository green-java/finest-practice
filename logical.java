public class logical {
    public static void main(String args[])
    {
        int m1=30,m2=75;
        //logical AND - both condition is true,o/p is true otherwise false
        System.out.println("Logical AND &&: "+(m1>=35&&m2>=35)); 
        //logical OR - both condition is false,o/p is true otherwise true
        System.out.println("Logical OR ||: "+(m1>=35||m2>=35)); 
    }   
}
